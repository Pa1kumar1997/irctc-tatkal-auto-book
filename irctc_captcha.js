function prettyPrint(arr) {
    var s = "";
    for (var i = 0; i < arr.length; i++) {
        var temp = arr[i];
        for (var k = 0; k < temp.length; k++) {
            s = s + temp[k];
        };
        s = s + "\n";
    };
}

