getPixelMapFromId = function(img) {
    var canvas;
    if(!canvas) {
        canvas = document.createElement('canvas');
        canvas.width = 170;
        canvas.height = 50;
        canvas.getContext('2d').drawImage(img, 0, 0, 170, 50);
    }
    var pixelMap = [];
    for (var i = 0; i < 50; i++) {
        var temp = [];
        for (var k = 0; k < 170; k++) {
            temp.push(Boolean(canvas.getContext('2d').getImageData(k, i, 1, 1).data[3]) ? 1 : 0);
        };
        pixelMap.push(temp);
    };
    return pixelMap;
};

function skip(start, end, y) {
    var flag = 0;
    for (var i = 0; i < start.length; i++) {
        if (y >= start[i] && y <= end[i]) {
            flag = 1;
            break;
        }
    }
    return flag;
};

function match_mask(rx, ry, image, mask) {
    var flag = 1;
    var breakflag = 0;
    var height = mask.length;
    var width = mask[0].length;
    var count = 0
    for (var x = 0; x < height; x++) {
        for (var y = 0; y < width; y++) {
            try {
                if (mask[x][y] == 1) {
                    if (image[rx+x][ry+y] == 1) {
                        count = count + 1;
                        continue;
                    }
                    else {
                        flag = 0;
                        breakflag = 1;
                        break;
                    }
                }
                else {
                    continue;
                }
            }
            catch(e) {
                flag = 0;
                breakflag = 1;
                break;
            }
        }
        if(breakflag) {
            break;
        }
    }
    if (count == 0){
        flag = 0;
    }
    return flag;
}

